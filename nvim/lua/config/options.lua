-- =============================
-- Wrap long lines in LazyVim
-- =============================
vim.opt.wrap = true        -- включить перенос строк по ширине окна
vim.opt.linebreak = true   -- переносить на границах слов
vim.opt.breakindent = true -- визуальный отступ для перенесённых строк
vim.opt.showbreak = "↪ "   -- символ, который показывает, что строка перенесена
